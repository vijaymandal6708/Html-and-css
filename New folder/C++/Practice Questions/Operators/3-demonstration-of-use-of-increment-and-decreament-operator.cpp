#include<iostream>
using namespace std;

int main()
{
  int a=12;
  // ++ increament operator
  // pre increament operator ++a
  cout<<++a;
  // pre increament operator a++
  cout<<endl<<a++;

  // decreament operator
  // pre decreament operator
  cout<<endl<<--a;
  // post decreament operator
  cout<<endl<<a--;
  return 0;
}